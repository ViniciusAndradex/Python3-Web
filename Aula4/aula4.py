"""
Tipos de Dados:
str - String
int - Inteiro
float - Real
bool - booleano/logico
"""
print('Luiz', type('Luiz'))
print(10, type(10))
print(25.23, type(25.23))
print(10 == 10, type(10 == 10))
print('L' == 'L', type('L' == 'L'))
print('*' * 10)
# Casting
print('Luiz', bool('luiz'))
print('10', type(int('10')))
# Exercicio
print('\nVinicius', type('Vinicius'))
print('20', type(int('20')))
print('1.85', type(float('1.85')))
print(20 >= 18, type(bool(20 >= 18)))
